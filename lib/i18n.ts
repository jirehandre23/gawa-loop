export type Locale = "en" | "fr" | "es" | "pt" | "ar";

export function detectLocale(): Locale {
  if (typeof window === "undefined") return "en";
  const saved = localStorage.getItem("gawa_locale") as Locale | null;
  if (saved && ["en","fr","es","pt","ar"].includes(saved)) return saved;
  const browser = navigator.language?.slice(0,2).toLowerCase();
  if (browser === "fr") return "fr";
  if (browser === "es") return "es";
  if (browser === "pt") return "pt";
  if (browser === "ar") return "ar";
  return "en";
}

export function setLocale(locale: Locale) {
  localStorage.setItem("gawa_locale", locale);
  window.location.reload();
}

